package method;

public class method01 {

    public static void fa1() {
        System.out.println("无返回值无参数");
    }

    public static void fa2(int a) {
        System.out.println("无返回值有参数");
    }

    public static int fa3() {
        System.out.println("有返回值无参数");
        return 20;
    }

    public static String fa4(int a) {
        System.out.println("有返回值有参数");
        return "20";
    }
}



